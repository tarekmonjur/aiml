import math

# ---------- Math Ceil and Floor Function ----------
x = 4.4
y = 4.1
print("Ceil & Floor:", math.ceil(x), math.floor(y))

# ---------- Math Round Function ----------
print("Round:", round(4.49), round(4.51))

# ---------- Math Power Function ----------
print("Power:", math.pow(2, 3))

# ---------- Math Square Root Function ----------
print("Square Root:", math.sqrt(15))

# ---------- Math Absolute Function ----------
print("Absolute:", math.fabs(-5))

# ---------- Math Factorial Function ----------
print("Factorial:", math.factorial(5))

# ---------- Math GCD Function ----------
print("GCD:", math.gcd(20, 30, 55))

